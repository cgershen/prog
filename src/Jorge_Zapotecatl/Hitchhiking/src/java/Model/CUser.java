package Model;

/**
 *
 * @author Jorge
 */
public class CUser {
    private int idU;
    private String name;
    private int age;
    private float latitude;
    private float longitude;

    public CUser(int idU, String name, int age, float latitude, float longitude) {
        this.idU = idU;
        this.name = name;
        this.age = age;
        this.latitude = latitude;
        this.longitude = longitude;
    }

    public int getIdU() {
        return idU;
    }

    public void setIdU(int idU) {
        this.idU = idU;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public float getLatitude() {
        return latitude;
    }

    public void setLatitude(float latitude) {
        this.latitude = latitude;
    }

    public float getLongitude() {
        return longitude;
    }

    public void setLongitude(float longitude) {
        this.longitude = longitude;
    }
    
    
}
